    private void jButtonCadastrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarClienteActionPerformed
        Cliente c = new Cliente();
        ClienteDAO dao = new ClienteDAO();

        c.setNomeCliente(jTextnomeCliente.getText());
        c.setFoneCliente(jTextfoneCliente.getText());
        c.setEmailCliente(jTextemailCliente.getText());
        dao.create(c);
        readJTable();
        jTextnomeCliente.setText("");
        jTextemailCliente.setText("");
        jTextfoneCliente.setText("");
    }//GEN-LAST:event_jButtonCadastrarClienteActionPerformed

    private void jTClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTClientesMouseClicked
        if(jTClientes.getSelectedRow() != -1){
            jTextnomeCliente.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(),1).toString());
            jTextfoneCliente.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(),2).toString());
            jTextemailCliente.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(),3).toString());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jTClientesMouseClicked

    private void jTClientesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTClientesKeyReleased
        if(jTClientes.getSelectedRow() != -1){
            jTextnomeCliente.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(),1).toString());
            jTextfoneCliente.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(),2).toString());
            jTextemailCliente.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(),3).toString());
        }
    }//GEN-LAST:event_jTClientesKeyReleased

    private void jButtonAtualizarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarClienteActionPerformed
        if(jTClientes.getSelectedRow() !=-1)
        {
            Cliente c = new Cliente();
            ClienteDAO dao = new ClienteDAO();

            c.setNomeCliente(jTextnomeCliente.getText());
            c.setFoneCliente(jTextfoneCliente.getText());
            c.setEmailCliente(jTextemailCliente.getText());
            c.setId((int)jTClientes.getValueAt(jTClientes.getSelectedRow(),0));
            dao.update(c);
            readJTable();
            jTextnomeCliente.setText("");
            jTextemailCliente.setText("");
            jTextfoneCliente.setText("");
        }
    }//GEN-LAST:event_jButtonAtualizarClienteActionPerformed

    private void jButtonDeletarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarClienteActionPerformed
        if(jTClientes.getSelectedRow() != -1){
            Cliente c = new Cliente();
            ClienteDAO dao = new ClienteDAO();

            c.setId((int)jTClientes.getValueAt(jTClientes.getSelectedRow(),0));
            dao.delete(c);
            readJTable();
            jTextnomeCliente.setText("");
            jTextemailCliente.setText("");
            jTextfoneCliente.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Selecioone um Produto para Excluir");
        }
       
    }//GEN-LAST:event_jButtonDeletarClienteActionPerformed


