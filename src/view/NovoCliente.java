     public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTClientes.getModel();
        modelo.setNumRows(0);
        ClienteDAO cdao = new ClienteDAO();
        cdao.read().stream().forEach((c) -> {
        modelo.addRow(new Object[]{
                c.getId(),
                c.getNomeCliente(),
                c.getFoneCliente(),
                c.getEmailCliente(),
            });
        });   
        }

