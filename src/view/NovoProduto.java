    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTable1.getSelectedRow() !=-1)
        {
            Produto p = new Produto();
            ProdutoDAO pdao = new ProdutoDAO();
            
            p.setNomeProduto(jTextFieldNomeProduto.getText());
            p.setPrecoProduto(Float.parseFloat(jTextFieldPrecoProduto.getText()));
            p.setId((int)jTable1.getValueAt(jTable1.getSelectedRow(),0));
            pdao.update(p);
            readTable();
            jTextFieldNomeProduto.setText("");
            jTextFieldPrecoProduto.setText("");
        } // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed
    

