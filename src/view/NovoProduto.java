    public void readTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();
        pdao.read().stream().forEach((c) ->{
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNomeProduto(),
                c.getPrecoProduto(),
            });
        });
    }
