    public void readJTableProduto(Produto id){
       
        DefaultTableModel modelo = (DefaultTableModel) jTableProduto.getModel();
        modelo.setNumRows(cont);
        ProdutoDAO cdao = new ProdutoDAO();
        
        cdao.read(id).stream().forEach((c) -> {
            modelo.addRow(new Object[]{
            c.getId(),
            c.getNomeProduto(),
            c.getPrecoProduto(),
            }); 
        });
      
        soma =soma + Float.parseFloat(jTableProduto.getValueAt(cont, 2).toString());
        cont++;
        jTextFieldTotal.setText(String.valueOf(soma));
    }
    public void readJTableProdutoRemove(Produto id){
        DefaultTableModel modelo = (DefaultTableModel) jTableProduto.getModel();
        modelo.setNumRows(cont);
        ProdutoDAO cdao = new ProdutoDAO();
        soma = soma - Float.parseFloat(jTableProduto.getValueAt(jTableProduto.getSelectedRow(),2).toString());
        modelo.removeRow(idproduto);
        cont--;
        jTextFieldTotal.setText(String.valueOf(soma));
    }

