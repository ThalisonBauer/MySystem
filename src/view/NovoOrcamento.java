    private void jButtonSalvarOrcamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarOrcamentoActionPerformed
        if(jComboBoxCliente != null){
            SimpleDateFormat fBr = new SimpleDateFormat("dd/MM/yyyy");
            Date dataAtual = new Date();

            Orcamento o = new Orcamento();
            OrcamentoDAO dao = new OrcamentoDAO();

            Produto p = new Produto();
            ProdutoDAO pdao = new ProdutoDAO();

            Cliente cliente = (Cliente) jComboBoxCliente.getSelectedItem();
            Produto produto = (Produto) jComboBoxNomeProduto.getSelectedItem();

            o.setTituloOrcamento(jTextFieldTituloOrcamento.getText());
            o.setIdCliente(cliente.getId());
            o.setDateOrcamento(fBr.format(dataAtual));
            o.setTotalOrcamento(soma);
            dao.createOrcamento(o);
            
            for(int i = 0; i<cont;i++)
            {
                int idpro = 0;
                int idorca = 0;
                idorca=dao.idOrcamento();
                idpro = Integer.parseInt(jTableProduto.getValueAt(i, 0).toString());
                dao.prod_orca(idorca, idpro);
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Campo vazio");
        }

    }//GEN-LAST:event_jButtonSalvarOrcamentoActionPerformed

